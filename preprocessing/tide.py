"""
preprocessing.tide - 固体潮汐改正（RG_ZONT2）
=============================================

实现 IERS Conventions 2010 中定义的 62 项待谐潮汐改正。
参考 GAMIT 源码 zont2.f，用于 UT1-UTC 预报的预处理环节。

核心算法:
    1. 根据修正儒略日（MJD）计算 5 个基本天文参数（L, LP, F, D, OM）
    2. 对 62 个潮汐分量求和，输出 UT1 或 LOD 的潮汐改正量

潮汐表（TIDE）共 62 行 × 6 列:
    列 0-1: UT1 的 sin/cos 系数
    列 2-3: LOD 的 cos/sin 系数
    列 4-5: UT1 的 cos/sin 系数（另一分量）

作者: 吴梓鹏
创建: 2025-05-25
"""

import numpy as np
import pandas as pd


# ============================================================================
# 辅助函数
# ============================================================================

def _mod(x: np.ndarray, y: float) -> np.ndarray:
    """浮点数取模（等价于 Fortran MOD 函数）。"""
    return x - y * (x / y).astype(int)


def _fundarg(T: np.ndarray) -> tuple:
    """
    计算 5 个基本天文参数（Delaunay 变量）。

    Args:
        T: 自 J2000.0 起的儒略世纪数

    Returns:
        (L, LP, F, D, OM): 5 个基本角（弧度）
    """
    DAS2R = 4.848136811095359935899141e-6   # 角秒 → 弧度
    TURNAS = 1296000.0                        # 一整圈的角秒数

    L = _mod(
        485868.249036 + T * (1717915923.2178 + T * (31.8792 + T * (0.051635 + T * (-0.00024470)))),
        TURNAS,
    ) * DAS2R

    LP = _mod(
        1287104.79305 + T * (129596581.0481 + T * (-0.5532 + T * (0.000136 + T * (-0.00001149)))),
        TURNAS,
    ) * DAS2R

    F = _mod(
        335779.526232 + T * (1739527262.8478 + T * (-12.7512 + T * (-0.001037 + T * (0.00000417)))),
        TURNAS,
    ) * DAS2R

    D = _mod(
        1072260.70369 + T * (1602961601.2090 + T * (-6.3706 + T * (0.006593 + T * (-0.00003169)))),
        TURNAS,
    ) * DAS2R

    OM = _mod(
        450160.398036 + T * (-6962890.5431 + T * (7.4722 + T * (0.007702 + T * (-0.00005939)))),
        TURNAS,
    ) * DAS2R

    return L, LP, F, D, OM


