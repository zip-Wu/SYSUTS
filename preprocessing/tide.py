def RG_ZONT2(MJD: np.ndarray, EOP: str = 'UT1') -> np.ndarray:
    """
    计算固体地球待谐潮汐改正量（RG_ZONT2，62 项）。

    参考: IERS Conventions 2010 / GAMIT 源码 zont2.f

    算法:
        - 根据 MJD 计算儒略世纪数 T
        - 计算 5 个基本天文参数
        - 对 62 个潮汐分量求和

    Args:
        MJD: 修正儒略日数组，形状 (n,)
        EOP: 输出类型，'UT1'（默认）或 'LOD'

    Returns:
        np.ndarray: 潮汐改正量数组。
            - EOP='UT1': 单位秒 (×1e-4)
            - EOP='LOD': 单位秒/天 (×1e-5)

    示例:
        >>> MJD = np.array([51544.5, 51545.5])
        >>> tide_ut1 = RG_ZONT2(MJD, 'UT1')
        >>> tide_lod = RG_ZONT2(MJD, 'LOD')
    """
    D2PI = 6.283185307179586476925287
    NZONT = 62

    T = (MJD - 51544.5) / 36525.0
    L, LP, F, D, OM = _fundarg(T)

    DLOD = 0.0
    DUT = 0.0

    for i in range(NZONT):
        ARG = _mod(
            _NFUND[i, 0] * L
            + _NFUND[i, 1] * LP
            + _NFUND[i, 2] * F
            + _NFUND[i, 3] * D
            + _NFUND[i, 4] * OM,
            D2PI,
        )
        ARG = np.where(ARG < 0.0, ARG + D2PI, ARG)

        DLOD += _TIDE[i, 2] * np.cos(ARG) + _TIDE[i, 3] * np.sin(ARG)
        DUT  += _TIDE[i, 0] * np.sin(ARG) + _TIDE[i, 1] * np.cos(ARG)

    if EOP == 'LOD':
        return DLOD * 1.0e-5
    elif EOP == 'UT1':
        return DUT * 1.0e-4
    else:
        return np.zeros_like(MJD)


def remove_tide_ut1(df_train: pd.DataFrame) -> np.ndarray:
    """
    计算 UT1 训练期的固体潮汐改正量。

    便捷函数，内部调用 RG_ZONT2(MJD, 'UT1')。

    Args:
        df_train: DataFrame，必须包含 'MJD' 列。

    Returns:
        np.ndarray: 训练期的潮汐改正量（秒），与 df_train 行数对齐。

    示例:
        >>> tide = remove_tide_ut1(df_train)
        >>> ut1r = ut1_tai - tide  # 去潮汐
    """
    MJD_train = df_train['MJD'].astype(float)
    return RG_ZONT2(MJD_train.values, 'UT1')
